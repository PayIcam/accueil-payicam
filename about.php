<h1 class="page-header">A propos</h1><br>

<h2>Objectif de PayIcam</h2>
<p>&emsp;PayIcam est une association qui a pour but de proposer un système de virement d’argent virtuel pouvant se substituer à tous les échanges d’argent liquide au sein de l’Icam, notamment pour : <br>
-	Les paiements au bar <br>
-	Les paiements à la cafet’ <br>
-	Les cotisations pour les assos <br>
-	Les billetteries (Gala, Spring, Œnologie…) <br>
-	Les remboursements entre élèves <br>
Le rôle de l’équipe de PayIcam est de veiller au bon fonctionnement de sa plateforme internet payicam.icam.fr ainsi qu’à sa bonne utilisation dans l’Icam. L’équipe se charge également du développement de la plateforme afin de fournir le service le plus complet possible tout en étant adapté aux besoins de l’Icam.
</p>

<h2>Engagement</h2>
<p>&emsp;L’équipe de PayIcam s’engage à maintenir sa plateforme au mieux afin d’assurer un service le plus performant possible conformément aux objectifs de l’association.<br>
Il est possible que des problèmes techniques surviennent sur PayIcam. Si c'est le cas, merci de nous prévenir, et nous ferons le maximum nécessaire pour les résoudre dans les meilleurs délais. Nous nous engageons à garder la transparence la plus totale envers les BDE de l’Icam quant à l’état de PayIcam.<br>
Les membres de PayIcam s’engagent également à ne demander aucune rémunération personelle pour les différents projets qu’ils seront amenés à conduire au sein de l’association. Nous nous réservons le droit de refuser un projet si nous jugeons que les conditions requises pour le mener à bien ne sont pas réunies. Ce jugement est à la discrétion du président de PayIcam et, en cas de refus d’un projet, la raison en sera toujours communiquée. </p>

<p>PayIcam n’est pas un outil de contrôle ni de sanction des élèves. En aucun cas nous ne communiquerons les données personnelles des élèves à qui que ce soit. </p>

<p>Un blocage de carte d’un ou plusieurs élèves est une mesure exceptionnelle qui ne saurait être imposée à PayIcam mais qui résulterait d’une discussion entre l’administration, le BDE, PayIcam et la fondation en question.
</p>

<h2>Origine de PayIcam</h2>
<p>&emsp;Le projet PayIcam fut lancé en 2013 avec pour objectif de diminuer les liquidités au sein de l’Icam de Lille ainsi que d’empêcher les vols de caisses du Bar qui se faisaient de plus en plus régulier. PayIcam provient directement de PayUTC qui était déjà en place à l’UTC depuis plusieurs années. Aujourd’hui, PayUTC a fusionné avec une boite d’évènementielle, créant ainsi Weezevent et est maintenant un prestataire professionnel de l’UTC. <br>
Il fallut une année pour adapter le système à l’Icam, mettre en place l’installation physique et réaliser les démarches auprès de la direction de l’Icam, du BDE et des différents fournisseurs.</p>

<p>PayIcam fut officiellement lancé durant l’année scolaire 2014-2015, essentiellement pour le Bar, la Cafet’ et le BDE. Au cours de cette année, 42 000€ ont transité par PayIcam, montrant ainsi la vitalité du projet.<br>
Par la suite, PayIcam a continué à se développer. En Janvier 2016, il y eut l’implantation de Shotgun et des inscriptions au Gala 2016.</p>

<h2>Et Maintenant ?</h2>
<p>Aujourd’hui, PayIcam prend en charge les ventes des évènements majeurs de l’Icam comme le Gala ou le Spring, leur fournissant un système de vente, une interface d'administration,  ainsi qu’une interface de contrôle et pointage des entrées. <br>
Nous nous occupons également des votes lors des différentes campagnes, améliorons en continue l’ergonomie de PayIcam et participons régulièrement à l’organisation d’évènements ponctuels. <br>
PayIcam fait désormais partie intégrante du site de Lille et vient de franchir la barre 400 000€ ayant transité par PayIcam.
L’association ne s’arrête pas là et s'est lancé sur le site de Toulouse début 2018, devenant ainsi la toute première association inter-sites de l’Icam.
</p>

<h2>Des projets en cours ?</h2>
<p>Nous avons toujours tout un tas de projets en cours ! N'hésitez d'ailleurs pas à nous proposer des ajustements, ou des projets également ! </p>

<p>Le Bar Trader, initialement développé par Robin Lacheré l'année dernière fut un franc succès, et reviendra bientôt ;) <br>
Nous travaillons également actuellement, à l'instauration d'un système de réservations et de payements de sandwichs à la cafétéria par PayIcam ! <br>
Pour le reste, vous verrez bien en temps venu !
</p>

